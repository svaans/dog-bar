export type StaffUiKey = keyof typeof STAFF.es;

export function staffT(key: StaffUiKey, lang: UiLang): string {
  return STAFF[lang][key] ?? STAFF.es[key];
}

/** Reemplaza `{n}`, `{visible}`, `{total}`, `{s}` (sufijo plural vacío o "s"). */
export function staffFill(
  template: string,
  vars: Record<string, string | number>,
): string {
  return template.replace(/\{(\w+)\}/g, (_, k: string) =>
    vars[k] !== undefined && vars[k] !== null ? String(vars[k]) : "",
  );
}
